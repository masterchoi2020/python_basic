# 연산자
print (1+1)
print (3-1)
print (5*2)
print (6/3)

print (2**3) # 2^3 = 8
print (5%3) # 나머지 구하기 2
print (10%3) # 1
print (5//3) # 몫 구하기 1
print (10//3)  # 3

print(10 > 3) # True
print(4 >= 7) # False
print(10 < 3) # False
print(5 <= 5) # True

print(3 == 3)  # 앞과 뒤가 같은지 확인
print(4 == 2)  # False
print(3 + 4 == 7)  # True

print(1 != 3)  # True 앞과 뒤가 같지 않다.
print(not (1 != 3))  # False앞과 뒤가 같지 않다.

print((3 > 0) and (3 < 5)) # and 연산 3이 0보다 크고 그리고 3이 5보다 작다
print((3 > 0) & (3 < 5)) # and 연산 3이 0보다 크고 그리고 3이 5보다 작다

print((3 > 0 ) or (3 > 5 )) # or 연산
print((3 > 0 ) | (3 > 5 ))

print( 5 > 4 > 3)  #True
print( 5 > 4 > 7)  #Falsee




