# 숫자 처리 함수
print(abs(-5))  # abs()함수: 절대값 계산
print(pow(4, 2)) # pow(4,2): 지수 승 계산(4^2)

print(max(5, 12))
print(min(5, 12))
print(round(3.14)) #반올림 : 3
print(round(4.99))  #반올림: 5

# math 라이브러리 이용
# math 라이브러리의 모든 것(*)을 이용하겠다
from math import *    
print(floor(4.99)) # 내림(버림 수): 4
print(ceil(3.14)) # 올림: 4
print(sqrt(16))  # sqrt()함수: 제곱근 계산 (4)
